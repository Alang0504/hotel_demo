package com.example.hotel_demo.model;

public class Season {
    private String status;
    private String oneCreatetime;
    private String twoCreatetime;
    private String threeCreatetime;

    public String getStatus() {
        return status;
    }

    public String getOneCreatetime() {
        return oneCreatetime;
    }

    public String getTwoCreatetime() {
        return twoCreatetime;
    }

    public String getThreeCreatetime() {
        return threeCreatetime;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public void setOneCreatetime(String oneCreatetime) {
        this.oneCreatetime = oneCreatetime;
    }

    public void setTwoCreatetime(String twoCreatetime) {
        this.twoCreatetime = twoCreatetime;
    }

    public void setThreeCreatetime(String threeCreatetime) {
        this.threeCreatetime = threeCreatetime;
    }
}
